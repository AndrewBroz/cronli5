// Ukrainian (uk) corpus — assembled from the ten reviewed Stage-2
// (corpus-translation) batches under tooling/experiments/uk-derive/
// (batch-1 .. batch-10), each translating a validated `en` donor file/range
// against the RATIFIED conventions in src/lang/uk/notes.md. Per CLAUDE.md
// ("The corpus is the contract"), this file — not any batch draft, and not
// a future renderer's output — is the oracle: src/lang/uk/index.js does not
// exist yet, so this corpus does not execute until the renderer is ported
// (Stage 4), but the rows are final now; the renderer is written to chase
// this corpus, never the other way around. See
// tooling/docs/language-pipeline.md and src/lang/uk/notes.md for the
// conventions every row below follows. The batch files are kept as the
// working record of translation intent (per-row rationale, flagged
// judgment calls) but are no longer the source consulted for test runs —
// this file is.
//
// Structure: one describe block per source batch (batch-1 .. batch-10, in
// that order), preserving each batch's own internal describe/run nesting
// and inline documentation verbatim. Batch-5 (originally plain exported
// arrays, since it predates this project's renderer even existing) is
// wrapped in describe/run blocks named after its own export names
// (secondLists, hourRanges, quartzL, ...) with no change to its rows. Only
// the shared `run` helper below and this header are new — they replace each
// batch draft's own duplicate copy of the same chai/mocha boilerplate.
//
// Ukrainian-specific conventions a reviewer should know before reading
// rows (full detail in src/lang/uk/notes.md): the 24-hour digital clock
// with no a.m./p.m. axis (о 9:00, unpadded, minutes always kept on the
// hour) and the asymmetric опівночі/о 12:00 дня midnight-noon pair (§1);
// the fully spelled genitive ordinal for a bare day-of-month (першого
// числа) vs digit + genitive month when a month is attached (1 січня)
// (§2); the always-inclusive, always-tagged range connective з … до …
// включно (§3, weekday ranges excepted per the round-3 reconciliation
// below); по + locative plural for EVERY recurring weekday qualifier —
// solo, list, or range (по понеділках, по буднях) — vs the accusative
// single dated occurrence (§4, §5); digits everywhere for cadence counts
// and list/
// position values, never spelled, except the bare-day-of-month ordinal
// carve-out above (§8); and the event-framed DOM-or-DOW union clause
// щоразу, коли настає X або Y (§7).

function run(cases) {
  cases.forEach(function each(values) {
    const pattern = values[0];
    const expected = values[1];
    const options = {...values[2] || {}, lang: uk};

    describe(JSON.stringify(pattern), function() {
      it('читається як "' + expected + '"', function() {
        expect(cronli5(pattern, options)).to.equal(expected);
      });
    });
  });
}

