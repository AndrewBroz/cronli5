// Build a human-review table: each cron pattern rendered by both
// libraries in English, Spanish, and Finnish, grouped so a reviewer can
// scan cronli5 against the matching cRonstrue locale per language.
//
// Writes tmp/review-trilingual.md under the project (gitignored).
// Usage: node scripts/review-trilingual.mjs

import {mkdirSync, readFileSync, writeFileSync} from 'node:fs';
import cronli5 from '../src/cronli5.js';
import cronstrueI18n from 'cronstrue/i18n.js';
import en from '../src/lang/en/index.js';
import es from '../src/lang/es/index.js';
import fi from '../src/lang/fi/index.js';

const cronstrueVersion = JSON.parse(readFileSync(
  new URL('../node_modules/cronstrue/package.json', import.meta.url),
  'utf8')).version;

const languages = [
  {code: 'en', label: 'English', lang: en},
  {code: 'es', label: 'Spanish', lang: es},
  {code: 'fi', label: 'Finnish', lang: fi}
];

// A spread of patterns chosen to exercise the constructions that differ
// across the three languages: clock and day-period words, weekday and
// month ranges (case pairs in Finnish, articles in Spanish), Quartz
// tokens, folded dates, compound multi-field folds, normalization, and
// the day-of-month-or-weekday alternation.
const sections = [
  ['Frequencies', [
    '* * * * *',
    '*/5 * * * *',
    '*/15 * * * *',
    '0 * * * *',
    '0 0 * * *',
    '*/15 * * * * *'
  ]],
  ['Clock times and day periods', [
    '0 9 * * *',
    '30 9 * * *',
    '0 12 * * *',
    '0 0 * * *',
    '0 1 * * *',
    '0 13 * * *',
    '0 9,17 * * *',
    '30 17 * * *'
  ]],
  ['Weekdays', [
    '0 9 * * MON',
    '0 9 * * SAT',
    '30 9 * * MON-FRI',
    '0 9 * * WED-FRI',
    '0 0 * * FRI-MON',
    '0 12 * * MON,WED,FRI'
  ]],
  ['Months and dates', [
    '0 12 1 1 *',
    '0 0 13 * *',
    '0 0 1,15 * *',
    '0 0 1-15 * *',
    '0 12 * 6,12 *',
    '0 12 * 11-2 *',
    '0 0 1 6-9 *',
    '0 0 12 25 12 * 2030'
  ]],
  ['Quartz tokens', [
    '0 0 L * *',
    '0 0 LW * *',
    '0 0 * * 5L',
    '0 0 * * MON#2',
    '0 0 15W * *'
  ]],
  ['Hour windows and steps', [
    '0 9-17 * * *',
    '0 22-2 * * *',
    '*/15 9-17 * * *',
    '* 9 * * *',
    '0 */2 * * *',
    '0 */5 * * *'
  ]],
  ['Compound and anchored', [
    '30 * * * *',
    '0,30 * * * *',
    '0-29 * * * *',
    '5,10 30 9 * * MON',
    '15 30 9 * * MON',
    '0,30 9 * * MON-FRI'
  ]],
  ['Normalization and either/or', [
    '1/1 * * * *',
    '0 9-9 * * *',
    '0 0 13 * FRI',
    '0 0 1,15 * 5',
    '59 23 31 12 5'
  ]]
];

function describe(fn, pattern) {
  try {
    return fn(pattern);
  }
  catch (error) {
    const message = error instanceof Error ? error.message : '' + error;

    return '*error: ' + message.replace(/`/g, '\'').replace(/\|/g, '\\|') +
      '*';
  }
}

function ours(pattern, language) {
  return describe(function render(p) {
    return cronli5(p, {lang: language.lang});
  }, pattern);
}

function theirs(pattern, language) {
  return describe(function render(p) {
    return cronstrueI18n.toString(p, {locale: language.code});
  }, pattern);
}

const out = [];

out.push('# Trilingual review: cronli5 vs. cRonstrue');
out.push('');
out.push('cronli5 (`src/`) and cRonstrue ' + cronstrueVersion +
  ' rendering the same patterns in English, Spanish, and Finnish. For a ' +
  'human reviewer: scan each language\'s two columns side by side. ' +
  'Generated by `scripts/review-trilingual.mjs`.');
out.push('');

for (const [title, patterns] of sections) {
  out.push('## ' + title);
  out.push('');

  for (const language of languages) {
    out.push('### ' + language.label + ' (`' + language.code + '`)');
    out.push('');
    out.push('| Pattern | cronli5 | cRonstrue |');
    out.push('| --- | --- | --- |');

    for (const pattern of patterns) {
      out.push('| `' + pattern + '` | ' + ours(pattern, language) +
        ' | ' + theirs(pattern, language) + ' |');
    }

    out.push('');
  }
}

const outDir = new URL('../tmp/', import.meta.url);
const outFile = new URL('review-trilingual.md', outDir);

mkdirSync(outDir, {recursive: true});
writeFileSync(outFile, out.join('\n') + '\n');
console.log('Wrote tmp/review-trilingual.md (cronstrue ' +
  cronstrueVersion + ').');
