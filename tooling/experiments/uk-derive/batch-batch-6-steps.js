// CANDIDATE corpus draft (batch-6-steps) — translated from the donor
// (en) files under test/lang/en/complex/steps/: strings.js,
// bounded-intervals.js, date-steps.js, minute-under-hour-step.js,
// month-steps.js, trivial-intervals.js, weekday-steps.js. Meaning is
// inherited from the (already-validated) donor rows; only the surface
// language is new. NOT yet human-reviewed — a draft for the add-language
// pipeline, per tooling/docs/language-pipeline.md. src/lang/uk/index.js
// does not exist yet, so this file is not runnable until the renderer is
// ported; it is written in the shape it will run in once it is.
//
// Conventions pinned from src/lang/uk/notes.md (deliberately DIFFERENT
// from the en donor's own forms — do not let them regress to en's shape):
//   - Clock time: digital, unpadded hour, minutes always kept ("о 9:00",
//     never a bare "о 9") — notes.md §1. Exact midnight is the adverb
//     "опівночі"; a RANGE start at midnight is the noun "з півночі"
//     (опівночі cannot follow "з", it is not a governable noun phrase).
//   - Numeral register: DIGITS everywhere for cadence counts and list/
//     range positions ("кожні 5 хвилин", "о 5-й та 10-й хвилині") — §8.
//     This is the opposite of en, which spells small cadence counts.
//   - A bare day-of-month VALUE (not a step's own interval) keeps en's
//     "always an ordinal" instinct but as a fully spelled genitive word
//     ("першого", "п'ятого числа") — §2. A day-of-month STEP's interval
//     itself is a cadence count, not a bare-day-of-month value, so it
//     takes the digit-ordinal register instead ("кожного 2-го дня
//     місяця") — this is a translation-time judgment call, not directly
//     dictated by §2, made to keep it a subordinate genitive-adverbial
//     the way "кожного дня" already works natively in Ukrainian.
//   - Range connective: "з … до … включно" (inclusive tag mandatory)
//     everywhere a range's closing boundary counts as a fire — §3.
//   - Recurrence marking: "по" + locative plural for ANY recurring solo
//     weekday or weekday list (regardless of whether en phrased it with
//     a leading "every" or a trailing "-s") — §4. A weekday RANGE keeps
//     the forced genitive-both-ends form instead ("з понеділка до
//     п'ятниці включно") — §5.
//   - Confinement (a coarser field subordinate to a finer cadence) is
//     marked by genitive case alone, never a second bare adverb
//     ("кожної години", "кожної 4-ї години") — playbook trap
//     confinement-vs-juxtaposition, §8.
//   - Bare MONTH lists repeat the locative preposition "у" per the
//     ratified worked example, with the і/й/та euphony rule applied
//     mechanically at each connector — §5, §3.
//   - No us/gb/house dialect axis exists for uk (notes.md "Anchors") —
//     the donor's one dialect-only row (gb serial-comma) is SKIPPED, see
//     note at that row below.

function run(cases) {
  cases.forEach(function each(values) {
    const pattern = values[0];
    const expected = values[1];
    const options = {...values[2] || {}, lang: uk};

    describe(JSON.stringify(pattern), function() {
      it('читається як «' + expected + '»', function() {
        expect(cronli5(pattern, options)).to.equal(expected);
      });
    });
  });
}

