// donor's English forms do NOT use (do not silently inherit the donor's
// forms):
//   - Clock: digital colon, unpadded hour, on-the-hour KEEPS ':00'
//     ('о 9:00', not 'о 9', never a 12-hour a.m./p.m. form) — notes.md §1.
//     'опівночі' for exact midnight.
//   - Bare day-of-month is the fully spelled genitive ordinal in EVERY
//     syntactic role below (single date, list, or range endpoint) —
//     'першого числа', 'п'ятнадцятого числа' — never the digit+hyphen
//     shorthand ('1-го'). notes.md §2 explicitly rules bare day-of-month
//     'regardless of how small the number is'; the digit form only surfaces
//     in §3/§7's own illustrative prose, not as a competing ratified
//     decision, so it is not let in here.
//   - A day number PAIRED with a month name is digit + genitive month, no
//     ordinal spelling, forced by grammar: '1 червня' — notes.md §2.
//   - Every range (clock, weekday, month, date, year) is 'з … до …
//     включно' — inclusiveness is lexically explicit every time, never a
//     silently inclusive/exclusive connective the way English's 'through'
//     vs 'until' is — notes.md §3. This also sidesteps English's own
//     range-wrap-through-zero failure mode (the donor phrases its 1-23 hour
//     range as 'through 11 p.m.' specifically to dodge an arithmetic
//     wraparound); the Ukrainian inclusive-by-construction range has no
//     such branch to guard.
//   - A weekday RANGE is genitive on both ends ('з понеділка до п'ятниці
//     включно'); a single dated weekday occurrence is accusative with в
//     ('в останню п'ятницю місяця'); a bare month list repeats the locative
//     preposition per item ('у січні, квітні, липні й жовтні') — notes.md
//     §5.
//   - The DOM/DOW inclusive union is the event-framed clause 'щоразу, коли
//     настає X' (never a copula calque, never a bracket correlative) —
//     every disjunct is nominative, the subject of 'настає' — notes.md §7.
//     A shared restricted month fronts once before the whole clause, never
//     straddling one arm only.
//   - A trailing/marked recurring position uses 'по' + locative plural —
//     notes.md §4 ratifies this for weekdays ('по понеділках'); this batch
//     extends the same device to the day-of-month PARITY class in its own
//     trailing/marked role ('по непарних днях місяця', for donor's 'on
//     every other day of the month'). That extension is this translator's
//     own consistent generalization, not itself a ratified §4 item — flagged
//     for panel confirmation alongside the rest of this candidate.
//   - Numeral register is digits everywhere for a cadence count or a list
//     POSITION (minute-of-hour, second-of-minute): 'о 5-й та 10-й хвилині'
//     is notes.md's own ratified worked example, reused verbatim here since
//     this exact donor phrase ('at 5 and 10 minutes past the hour') is what
//     that example was worked against — notes.md §8. This does not touch
//     bare day-of-month, which stays spelled per §2 regardless.
//   - Weekday/month names stay lowercase (Ukrainian orthography does not
//     capitalize them), unlike the donor's capitalized 'Tuesday'/'January'.
//
// SKIPPED: none. All 88 donor rows in this range (65 OR-union + 23
// workflow-found) are transferable and translated below — none of this
// range is donor-typography-only (every row carries a union frame, a range,
// a list, or a numeral position that has a real Ukrainian equivalent).
//
// Row shape: [pattern, expected, opts?] — pattern is EXACT from the donor;
// opts (none in this batch) would carry over unchanged where the donor used
// one.

function run(cases) {
  cases.forEach(function each(values) {
    const pattern = values[0];
    const expected = values[1];
    const options = {...values[2] || {}, lang: uk};

    describe(JSON.stringify(pattern), function() {
      it('читається як "' + expected + '"', function() {
        expect(cronli5(pattern, options)).to.equal(expected);
      });
    });
  });
}

